class Solution:
    def numberOfMatches(self, n: int) -> int:
        # 7==6/2+1=4/2=2/2=1===>3+2+1=6
        # 14==14/2=7==1+6/2=4/2=2/2=1===>7+3+2+1=14-1
        # 16==16/2=8/2=4/2=2/2=1===>8+4+2+1=15=16-1
        # 18=18/2=9=1+8/2=4/2=2/2=1==>9+4+2+1=16
        # 19=1+18/2=9==1+8/2=4/2=2/2=1===>9+4+2+1=16
        match=0
        while n!=1:
            if n%2==0:
                match+=n/2
                n-=n/2
            else:
                match+=(n-1)/2
                n-=(n-1)/2
                
        return int(match)
                
            